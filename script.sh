#!/bin/bashi

var1=$1
var2=$2

let addition=$var1+$var2
let substraction=$var1-$var2
let multiplication=$var1*$var2
let division=$var1/$var2

echo "The result of the addition is: $addition"
echo "The result of the substraction is: $substraction"
echo "The result of the multiplication is: $multiplication"
echo "The result of the division is: $division"

